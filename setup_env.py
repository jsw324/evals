def test_api_connection(base_url: str, agent_id: str, token: str):
    """Test the API connection"""
    
    try:
        import requests
        
        print("\nðŸ” Testing API connection...")
        
        headers = {
            'Content-Type': 'application/json'
        }
        
        # Add auth header only if token is provided
        if token:
            headers['Authorization'] = f'Bearer {token}'
            print(f"Using authentication: Bearer {token[:8]}...{token[-8:] if len(token) > 16 else '***'}")
        else:
            print("No authentication (dev mode)")
        
        response = requests.get(f"{base_url}/{agent_id}", headers=headers, timeout=10)
        
        if response.status_code == 200:
            print("âœ… API connection successful!")
            print("ðŸŽ‰ Your evaluation system is ready to use!")
        elif response.status_code == 401:
            print("âŒ Authentication failed - check your API token")
        elif response.status_code == 404:
            print("âŒ Agent not found - make sure 'agentuity dev' is running")
        else:
            print(f"âš ï¸  Unexpected response: {response.status_code}")
            print(f"Response: {response.text[:200]}...")
            
    except ImportError:
        print("âš ï¸  requests library not found. Install dependencies first: uv sync")
    except Exception as e:
        print(f"âŒ Connection test failed: {e}")
        print("ðŸ’¡ Make sure 'agentuity dev' is running in another terminal")

